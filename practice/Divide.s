	AREA RESET, DATA, READONLY
	EXPORT __Vectors
__Vectors
	DCD 0X10001000
	DCD Reset_Handler
	ALIGN
	AREA mycode, CODE, READONLY
	ENTRY
	EXPORT Reset_Handler
Reset_Handler
	LDR R0, =N1
	LDR R0, [R0]
	LDR R1, =N2
	LDR R1, [R1]
	MOV R2, #0 		;QUOTIENT
LOOP
	CMP R0, R1
	BLT DONE
	SUB R0, R0, R1
	ADD R2, R2, #1
	B LOOP
DONE
	LDR R3, =QUOT
	STR R2, [R3]
	LDR R4, =REM
	STR R0, [R4]
STOP B STOP
N1 DCD 123
N2 DCD 0XA
	AREA mydata, DATA, READWRITE
QUOT DCD 0
REM DCD 0
	END