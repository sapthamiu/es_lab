	AREA RESET, DATA, READONLY
	EXPORT __Vectors
__Vectors
	DCD 0x10001000
	DCD Reset_Handler
	ALIGN
	AREA mycode, CODE, READONLY
	ENTRY
	EXPORT Reset_Handler
Reset_Handler
	LDR R0, =ARR
	LDR R1, =N
	LDR R1, [R1]
	SUB R1, R1, #1			;N-1
	ADD R8, R0, R1, LSL #2	;R8 = R0 + (N-1) * 4 LAST ELE INDEX
ILOOP
	MOV R2, R0				;CURRENT MIN INDEX
	ADD R3, R0, #4			;NEXT ELE INDEX
JLOOP
	CMP R3, R8				;CHECK IF END OF ARRAY
	BHI SWAP
	
	LDR R4, [R3]			;CURRENT ELE
	LDR R5, [R2]			;MIN
	CMP R4, R5
	BLO NEWMIN
	ADD R3, R3, #4
	B JLOOP
NEWMIN
	MOV R2, R3
	ADD R3, R3, #4
	B JLOOP

SWAP
	LDR R6, [R0]
	LDR R7, [R2]
	STR R7, [R0]
	STR R6, [R2]
	
	ADD R0, R0, #4
	SUBS R1, R1, #1
	BNE ILOOP
STOP B STOP
N DCD 5
	AREA mydata, DATA, READWRITE
ARR DCD 5, 4, 3, 2, 1
	END
