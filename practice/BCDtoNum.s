__Vectors
	DCD 0X10001000
	DCD Reset_Handler
	ALIGN
	AREA MYCODE, CODE, READONLY
	ENTRY
	EXPORT Reset_Handler
Reset_Handler
	LDR R0, =SRC
	MOV R1, #8		;8 NIBBLES
	LDR R2, =DST
LOOP
	LDRB R3, [R0], #1
	AND R4, R3, #0X0F
	STR R4, [R2], #4
	AND R4, R3, #0XF0
	LSR R4, #4
	STR R4, [R2], #4
	SUBS R1, R1, #2
	BNE LOOP
STOP B STOP
SRC DCD 0X12345678
	AREA MYDATA, DATA, READWRITE
DST DCD 0
	END
		