	AREA RESET, DATA, READONLY
	EXPORT __Vectors
__Vectors
	DCD 0X10001000
	DCD Reset_Handler
	ALIGN
	AREA mycode, CODE, READONLY
	ENTRY
	EXPORT Reset_Handler
Reset_Handler
	LDR R0, =N
	LDR R0, [R0]
	MOV R1, #0			;FIRST TERM
	MOV R2, #1			;SECOND TERM
	LDR R3, =START
	STR R1, [R3], #4
	STR R2, [R3], #4
	SUB R0, R0, #2
LOOP
	ADD R9, R1, R2		;NEW TERM
	STR R9, [R3], #4
	MOV R1, R2
	MOV R2, R9
	SUBS R0, R0, #1
	BNE LOOP
STOP B STOP
N DCD 7
	AREA mydata, DATA, READWRITE
START DCD 0
	END