LOOP
	LDRB R3, [R0], #1
	AND R4, R3, #0X0F		;8
	ADD R4, R4, #0X30
	STR R4, [R2], #4
	AND R5, R3, #0XF0		;7
	LSR R5, #4
	ADD R5, R5, #0X30
	STR R5, [R2], #4
	SUBS R1, R1, #1
	BNE LOOP
STOP B STOP
