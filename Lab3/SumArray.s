Reset_Handler
	LDR R0, =ARR
	LDR R1, =N
	LDR R1, [R1]
	LDR R2, [R0], #4		;SUM
	SUB R1, R1, #1
LOOP
	CMP R1, #0
	BEQ DONE
	LDR R3, [R0], #4
	ADD R2, R2, R3
	SUB R1, R1, #1
	B LOOP
DONE
	LDR R4, =SUM
	STR R2, [R4]
STOP B STOP
ARR DCD 1, 2, 3, 4, 5
N DCD 5
	AREA mydata, DATA, READWRITE
SUM DCD 0
	END