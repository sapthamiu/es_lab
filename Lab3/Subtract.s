Reset_Handler
	LDR R0, =N1
	LDR R0, [R0]
	LDR R1, =N2
	LDR R1, [R1]
	SUB R2, R0, R1		;RSB R2, R0, R1 FOR R1-R0
	LDR R3, =DIF
	STR R2, [R3]
STOP B STOP
