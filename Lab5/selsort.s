	AREA RESET, DATA, READONLY
	EXPORT __Vectors
__Vectors
	DCD 0x10001000
	DCD Reset_Handler
	ALIGN
	AREA mycode, CODE, READONLY
	ENTRY
	EXPORT Reset_Handler
Reset_Handler
     LDR R0, =ARRAY
	 LDR R1, =SIZE
	 LDR R1, [R1]
	 SUB R1, R1, #1				;OUTERLOOP RUNS N-1 TIMES
	 ADD R8, R0, R1, LSL #2		;CALCULATE LAST ELE
OUTERLOOP
	CMP R1, #0
	BLT STOP					;ALL ELEMENTS SORTED
	MOV R2, R0					;CURRENT MIN(Ith ELE)
	MOV R3, R0					;Ith ELE
	ADD R3, R3, #4				;START FROM (I+1)th ELE
INNERLOOP
	CMP R3, R8					;CHECK IF ELE IS LOWER THAN LAST ELE
	BHI SWAP					;SWAP IF END IS REACHED
	LDR R4, [R3]				;LOAD CURRENT ELE
	LDR R5, [R2]				;LOAD MIN
	CMP R4, R5			
	BLT UPDATEMIN				;MIN = min(MIN, ELE)
	ADD R3, R3, #4
	B INNERLOOP

UPDATEMIN
	MOV R2, R3
	B INNERLOOP

SWAP
	LDR R6, [R0]				;FIRST ELE
	LDR R7, [R2]				;MIN
	STR R7, [R0]
	STR R6, [R2]
	
	
	ADD R0, R0, #4				;NEXT ELE AS FIRST ELE
	SUB R1, R1, #1
	B OUTERLOOP
STOP
	B STOP
SIZE   DCD 6
	AREA mydata, DATA, READWRITE
ARRAY  DCD 5, 3, 8, 6, 2, 4
	END
