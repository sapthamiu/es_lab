	MOV R1, #4		;4 BYTES
	MOV R2, #0		;RESULT
	MOV R4, #0 		;COUNTER
LOOP
	LDRB R3, [R0], #1
	AND R5, R3, #0X0F
	LSL R5, R4
	ORR R2, R2, R5
	ADD R4, R4, #4
	SUBS R1, R1, #1
	BNE LOOP
	LDR R6, =DST
	STR R2, [R6]

STOP B STOP
